
# Subscripting
print("Hello"[0]) # This prints the first character of the string
print("Hello"[-1]) # This prints the last character of the string

# Integer = Whole numbers
integer_number = 1 # This is an integer
print(123012 + 193213) # These are both integers

# Large Integers
print(123_456_789) # This prints out "123456789", the underlines are added purely for human visualization

# Float = Floating point number
print(3.14159) # This is a float

# Boolean = True and False (maybe 1 or 0)
print(True)
print(False)